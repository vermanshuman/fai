MERGE INTO FAI_NO_GAP_SEQUENCE tgt
USING
(
SELECT
   ?ACRONYM?     ACRONYM
  ,1             LAST_VALUE
FROM DUAL
) src
ON (tgt.ACRONYM=src.ACRONYM)
WHEN MATCHED
THEN UPDATE SET
   tgt.LAST_VALUE = tgt.LAST_VALUE+1
WHEN NOT MATCHED
THEN INSERT
(
   OID
  ,ACRONYM
  ,LAST_VALUE
)
VALUES
(
   OID.NEXTVAL
  ,src.ACRONYM
  ,src.LAST_VALUE
)
